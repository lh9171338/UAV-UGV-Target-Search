/*----- Constructors -----*/
JMD::JMD_Utils_Object::JMD_Utils_Object() : Value(0) {}

JMD::JMD_Utils_Object::JMD_Utils_Object(JMD::JMD_Utils_Object const & obj_param) : Value(obj_param.Value ? obj_param.Value->clone() : 0) {}
/*--- End Constructors ---*/

/*----- Destructors -----*/
JMD::JMD_Utils_Object::~JMD_Utils_Object() { delete Value; }
/*--- End Destructors ---*/

/*----- Operator Overloads -----*/
JMD::JMD_Utils_Object& JMD::JMD_Utils_Object::operator=(const JMD::JMD_Utils_Object& obj_param)
{
	if(&obj_param != this)
	{
		JMD_Utils_Object copy(obj_param);
		swap(copy);
	}
	return *this;
}
/*--- End Operator Overloads ---*/

/*----- Methods -----*/
void JMD::JMD_Utils_Object::swap(JMD::JMD_Utils_Object& obj_param)
{
	std::swap(Value, obj_param.Value);
}
/*--- End Methods ---*/

